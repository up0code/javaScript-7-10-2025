let fruits = ['apple', 'banana'];

// Adding elements
fruits.push('orange'); // Add to end → ['apple', 'banana', 'orange']
fruits.unshift('grape'); // Add to beginning → ['grape', 'apple', 'banana', 'orange']

// Removing elements
fruits.pop(); // Remove from end → ['grape', 'apple', 'banana']
fruits.shift(); // Remove from beginning → ['apple', 'banana']

// Modifying specific element
fruits[1] = 'kiwi'; // ['apple', 'kiwi']



